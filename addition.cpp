#include <iostream>
using namespace std;
int main(){
  cout << "18と63の和は" << 18 + 63 << "です。\n";

  int x, y;
  x = 18;
  y = 63;
  cout << "x:" << x << "\n";
  cout << "y:" << y << "\n";
  cout << "The sum of x and y:" << x + y << "\n";
  cout << "The average of x and y:" << (x + y) / 2 << "\n";

}
